def get_all_values(nested_dicts: dict, key):
    all_values = set()
    if key in nested_dicts:
        all_values.add(nested_dicts[key])
    for v in nested_dicts.values():
        if type(v) == dict:
            value = get_all_values(v, key)
            if value is not None:
                all_values.update(value)
    return all_values