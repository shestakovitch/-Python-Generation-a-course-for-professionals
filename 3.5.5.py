from datetime import datetime

pattern = '%d.%m.%Y'
dct = {}
for i in range(int(input())):
    *name, dt = input().split()
    dt = datetime.strptime(dt, pattern)
    name = ' '.join(name)
    dct.setdefault(dt, []).append(name)
if len(dct[min(dct)]) == 1:
    print(min(dct).strftime(pattern), *dct[min(dct)])
else:
    print(min(dct).strftime(pattern), len(dct[min(dct)]))