import json
import csv

groceries, rows, s = {}, [], 0
for i in range(1, 5):
    with open(f'quarter{i}.csv', encoding='utf-8') as file:
        rows.extend(list(csv.reader(file))[1:])
for row in rows:
    groceries[row[0]] = groceries.get(row[0], 0) + int(row[1]) + int(row[2]) + int(row[3])
with open('prices.json', encoding='utf-8') as price:
    data = json.load(price)
for k, v in groceries.items():
    s += v * data[k]
print(s)