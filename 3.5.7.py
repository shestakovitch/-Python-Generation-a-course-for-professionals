from datetime import datetime, timedelta

pattern = '%d.%m.%Y'
current_date = datetime.strptime(input(), pattern)
dct = {}
for i in range(int(input())):
    *name, dt = input().split()
    name, dt = ' '.join(name), datetime.strptime(dt, pattern)
    dct[dt] = name

res = []
for i in sorted(dct, reverse=True):
    data_ch_year = datetime(year=i.year, month=current_date.month, day=current_date.day)
    if data_ch_year < i <= data_ch_year + timedelta(days=7) or data_ch_year < i + timedelta(days=365) <= data_ch_year + timedelta(days=7):
        res.append(dct[i])

