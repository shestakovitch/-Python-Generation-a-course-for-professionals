from datetime import datetime

pattern = '%d.%m.%Y'
dct = {}
for i in range(int(input())):
    *name, dt = input().split()
    name, dt = ' '.join(name), datetime.strptime(dt, pattern)
    dct.setdefault(dt, []).append(name)
mx_ln = max([len(v) for v in dct.values()])
print(*[k.strftime(pattern) for k, v in sorted(dct.items(), key=lambda x: (x[0], len(x[1]))) if len(v) == mx_ln], sep='\n')