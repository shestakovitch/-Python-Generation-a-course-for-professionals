def get_biggest(numbers=list):
    if not numbers:
        return -1
    else:
        l = len(str(sorted(numbers, key=lambda x: len(str(x)))[-1]))
        numbers = map(str, sorted(numbers, key=lambda x: str(x) * l, reverse=True))
        return int(''.join(numbers))